export function useLang() {
  const [lang, setLangState] = useState("fr");
  useEffect(() => {
    try { const l = localStorage.getItem("arcardi_lang"); if (l === "en" || l === "fr") setLangState(l); } catch (e) {}
  }, []);
  const setLang = (l) => { setLangState(l); try { localStorage.setItem("arcardi_lang", l); } catch (e) {} };
  const t = (k) => (STR[lang] && STR[lang][k]) ?? STR.fr[k] ?? k;
  return { lang, setLang, t };
}
